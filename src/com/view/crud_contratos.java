    private void btnElegirServicioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirServicioActionPerformed
        // TODO add your handling code here:

        int codigo = Integer.parseInt(JOptionPane.showInputDialog("Ingrese el codigo del servicio"));
        servicio = servicioCtr.findServicio(codigo);
        if (!plan.getServicioList().isEmpty()) {
            if (plan.getServicioList().contains(servicio)) {
                JOptionPane.showMessageDialog(null, "Ya ingresó este servicio");
            } else {
                plan.getServicioList().add(servicio);
                costoTotal += servicio.getCostoEnPlan();
                addServicioAPlan(servicio);

            }
        } else {
            plan.getServicioList().add(servicio);
            btnCrearPlan.setEnabled(true);
            costoTotal += servicio.getCostoEnPlan();
            addServicioAPlan(servicio);
        }
    }//GEN-LAST:event_btnElegirServicioActionPerformed

    private void btnCrearPlanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearPlanActionPerformed
        // TODO add your handling code here:
        if (cbCLiente.getSelectedIndex() != 0) {
            try {
                String str = cbCLiente.getSelectedItem().toString();
                String[] newStr = str.split("\\s+");
                int codigoCliente = Integer.parseInt(newStr[0]);
                cliente = afiliadoCTR.findAfiliado(codigoCliente);
                if (!cliente.getComprobanteList().isEmpty()) {
                cbCLiente.setEnabled(false);
                plan.setNumeroBeneficiarios(cliente.getBeneficiarioList().size());
                List ls = planCtr.findPlanEntities();
                int codigoPlan = ls.size() + 1;
                plan.setCodigo(codigoPlan);
                txt_codigoPlan.setText(codigoPlan + "");
                
                    
                    JOptionPane.showMessageDialog(null, "El cliente ya tiene asignado un plan porfavor eliminelo para crear uno nuevo o modifiquelo");
                } else {
                    btnElegirServicio.setEnabled(false);
                    btnGenerarComprobante.setEnabled(true);
                    btnCrearPlan.setEnabled(false);
                    plan.setCostoTotal(costoTotal);
                    // planCtr.create(plan);
                    JOptionPane.showMessageDialog(null, "Plan creado");
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seleccione un cliente");
        }
    }//GEN-LAST:event_btnCrearPlanActionPerformed


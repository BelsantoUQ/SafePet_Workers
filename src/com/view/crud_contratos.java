    private void crearTablaServiciosSelect() {

        String col[] = {"ID", "NOMBRE", "COPAGO", "COSTO"};
        modeloServicios = new DefaultTableModel(col, 0);
    }

    private void getComboBox() {
        List ls;
        ls = afiliadoCTR.findAfiliadoEntities();
        String value = "";
        for (int i = 0; i < ls.size(); i++) {
            cliente = (Afiliado) ls.get(i);
            value = cliente.getCodigo() + " " + cliente.getNombre();
            cbCLiente.addItem(value);
        }
    }

    private void getTablaServicios() {
        String col[] = {"ID", "NOMBRE", "COPAGO", "COSTO"};
        DefaultTableModel modelo = new DefaultTableModel(col, 0);
        Object obj[] = new Object[5];
        List ls;
        try {
            ls = servicioCtr.findServicioEntities();
            for (int i = 0; i < ls.size(); i++) {
                servicio = (Servicio) ls.get(i);
                obj[0] = servicio.getCodigo();
                obj[1] = servicio.getNombre();
                obj[2] = servicio.getCopago();
                obj[3] = servicio.getCostoEnPlan();
                modelo.addRow(obj);
            }
            this.jTableServiciosC.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar persistencia");

        }

    }

    private void addServicioAPlan(Servicio servicioAux) {
        Object obj[] = new Object[5];
        try {

            obj[0] = servicioAux.getCodigo();
            obj[1] = servicioAux.getNombre();
            obj[2] = servicioAux.getCopago();
            obj[3] = servicioAux.getCostoEnPlan();
            modeloServicios.addRow(obj);

            this.jTableSeleccionado.setModel(modeloServicios);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar persistencia");

        }

    }


    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String codigo = (JOptionPane.showInputDialog("Ingrese el codigo de contrato"));
        comprobante = comprobanteCtr.findComprobante(Integer.parseInt(codigo));
        if (comprobante !=null) {
            txt_contrato.setText(codigo);
            txtCliente.setText(comprobante.getIdAfiliado().getCodigo()+"");
            lblUser.setText(comprobante.getIdAfiliado().getNombre());
            btnEliminar.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(null, "No se encontro un contrato con este codigo");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed


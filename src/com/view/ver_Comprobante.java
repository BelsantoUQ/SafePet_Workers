    private void getTabla() {
        String col[] = {"ID", "MEDIO DE PAGO", "CLIENTE", "COSTO"};
        DefaultTableModel modelo = new DefaultTableModel(col, 0);
        Object obj[] = new Object[4];
        List ls;
        try {
            ls = comprobanteCtr.findComprobanteEntities();
            for (int i = 0; i < ls.size(); i++) {
                comprobante = (Comprobante) ls.get(i);
                obj[0] = comprobante.getCodigo();
                obj[1] = comprobante.getMedioDePago();
                obj[2] = comprobante.getIdAfiliado().getNombre();
                obj[3] = comprobante.getIdPlan().getCostoTotal();
                modelo.addRow(obj);
            }
            this.jTablaComprobantes.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error al cargar persistencia");

        }

    }

